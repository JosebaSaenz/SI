package Proiektua;

public class TuplaAntzekotasuna {
	
	private int id;
	private double antzekotasuna;
	
	public TuplaAntzekotasuna(int pId, double pAntzekotasuna) {
		id = pId;
		antzekotasuna = pAntzekotasuna;
	}

	public double getAntzekotasuna() {
		return antzekotasuna;
	}

	public int getId() {
		return id;
	}
	
}
